# 通过type()查看变量类型

# int
# <class 'int'>
a = 1
print(type(a))

# float
# <class 'float'>
b = 1.1
print(type(b))

# boolean
# <class 'bool'>
c = True
print(type(c))

# string
# <class 'str'>
d = 'ad'
print(type(d))

# list
# <class 'list'>
e = [1,2,3]
print(type(e))

# tuple
# <class 'tuple'>
f = (1,2,3)
print(type(f))

# dict
# <class 'dict'>
g = {'name':'zs'}
print(type(g))